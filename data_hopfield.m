%add noise
function noisepattern=noise_with_pattern(pattern)
    noise = 0.25;
    [rows, cols] = size(pattern);
    sizenum = rows*cols;
    numchangepoint = noise*sizenum;
    noisepattern=pattern;
    indices_to_flip = randperm(sizenum, numchangepoint);
    noisepattern(indices_to_flip) = -noisepattern(indices_to_flip);
end

function drawPattern(pattern)
    [rows, cols] = size(pattern);

    xBlack = [];
    yBlack = [];
    xWhite = [];
    yWhite = [];

    for row = 1:rows
        for col = 1:cols
            if pattern(row, col) == 1
                xBlack = [xBlack, col];
                yBlack = [yBlack, row];
            elseif pattern(row, col) == -1
                xWhite = [xWhite, col];
                yWhite = [yWhite, row];
            end
        end
    end

    scatter(xBlack, yBlack, 'k', 'filled');
    hold on;
    scatter(xWhite, yWhite, 'w', 'filled');
    hold off;

    axis([1 cols 1 rows]);
    axis ij;
    axis square;
end

